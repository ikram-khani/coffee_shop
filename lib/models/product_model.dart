class Product {
  final int id;
  final String name;
  final String description;
  final double price;
  final String pictureUrl;
  final String category;
  final double rating;
  final int reviews;

  Product({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.pictureUrl,
    required this.category,
    required this.rating,
    required this.reviews,
  });
}
